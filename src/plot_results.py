"""
Plot open quantum system dynamics generated by run_oqupy_dynamics.py.
"""

from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np


def main() -> None:
    root = Path(__file__).resolve().parents[1]
    results_path = root / "results" / "oqupy_open_system_dynamics.npz"
    figures_dir = root / "figures"
    figures_dir.mkdir(exist_ok=True)

    if not results_path.exists():
        raise FileNotFoundError(
            f"Results file not found: {results_path}\n"
            "Run `python src/run_oqupy_dynamics.py` first."
        )

    data = np.load(results_path)

    times = data["times"]
    sigma_z = data["sigma_z"]
    sigma_x = data["sigma_x"]
    alpha = float(data["alpha"])
    omega_cutoff = float(data["omega_cutoff"])

    plt.figure(figsize=(7, 4.5))
    plt.plot(times, sigma_z, linewidth=2, label=r"$\langle \sigma_z(t)\rangle$")
    plt.plot(times, sigma_x, linewidth=2, label=r"$\langle \sigma_x(t)\rangle$")
    plt.xlabel("Time")
    plt.ylabel("Expectation value")
    plt.title(
        rf"OQuPy Open-System Dynamics: $\alpha={alpha}$, "
        rf"$\omega_c={omega_cutoff}$"
    )
    plt.legend()
    plt.tight_layout()

    output_path = figures_dir / "open_system_expectation_values.png"
    plt.savefig(output_path, dpi=300)
    plt.close()

    print(f"Saved figure: {output_path}")


if __name__ == "__main__":
    main()
